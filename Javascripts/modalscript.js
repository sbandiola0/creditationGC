const modalContainers = document.querySelectorAll('.modalcontainer');

function tabs(panelIndex, modalContainer) {
	const tabs = modalContainer.querySelectorAll('.tabShow');
	const selectionElement = modalContainer.querySelector('.selection');

	tabs.forEach(function (node) {
		node.style.display = 'none';
	});

	tabs[panelIndex].style.display = 'block';

	switch (panelIndex) {
		case 0:
		case 3:
		case 6:
		case 9:
		case 12:
		case 15:
		case 18:
		case 21:
		case 24:
			selectionElement.textContent = 'Area';
			break;

		case 1:
		case 4:
		case 7:
		case 10:
		case 13:
		case 16:
		case 19:
		case 22:
		case 25:
			selectionElement.textContent = 'Preliminary Documents';
			break;

		case 2:
		case 5:
		case 8:
		case 11:
		case 14:
		case 17:
		case 20:
		case 23:
		case 26:
			selectionElement.textContent = 'Online Observation';
			break;

		default:
			break;
	}
}

function activateModal(modalContainer) {
	const tabBtns = modalContainer.querySelectorAll('.li a');

	tabs(0, modalContainer); // Show the initial tab

	tabBtns.forEach(function (btn, index) {
		btn.addEventListener('click', function (event) {
			event.preventDefault();
			tabs(index, modalContainer); // Activate the selected tab
		});
	});
}

modalContainers.forEach(function (container) {
	const closeButton = container.querySelector('.close');

	closeButton.addEventListener('click', function () {
		container.style.display = 'none';
	});

	activateModal(container);
});

